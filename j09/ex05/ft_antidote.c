int		ft_antidote(int i, int j, int k)
{
	if ((i < j && j < k) || (k < j && j < i))
		return (j);
	if ((j < i && i < k) || (k < i && i < j))
		return (i);
	if ((j < k && k < i) || (i < k && k < j))
		return (k);
	else
		return (0);
}
